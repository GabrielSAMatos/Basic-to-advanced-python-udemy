#Type int and float
#int -> whole number
#The type int represents any number
#positive or negative. unsigned int is considered positive
print(1)
print(-1)
print(0, end= '\n-------------\n')

#float -> Floating point number
#The type float represent any number
#positive or negative with floating point
#float unsigned is considered positive 
print(1.1)
print(-1.1)
print(0.0, end= '\n-------------\n')

#the function "type" shows the type that Python inferred to the valor
print(type(1))
print(type(1.1))
print(type(0))
print(type(0.0))